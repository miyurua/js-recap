const food = ["apple", "carrot", "pear", "banana", "pineapple", "avocado"];

/** Method 01 - forEach
 * params - call back function
 * mainly to execute the same code on all the elements in an array
 * 
 * output --> 
apple
carrot
pear
banana
pineapple
avocado
 */
food.forEach((item) => {
  console.log(item);
});
